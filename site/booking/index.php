		console.info($('#sd').val().length);
		if(0 < $('#sd').val().length) {
			$('#sn').prop('disabled','');
			$('#sn').attr('title','');
			$('#snNote').fadeOut();
		}
		else{
			$('#sn').prop('disabled','disabled');
			$('#sn').attr('title','泊数を指定するには、チェックイン日を指定します。');
			$('#snNote').show();
			$('#sn').val("1");
		}
