$(function() {
	$("#areaMap .map").hide();
	$("#areaMenu a").hover(function(){
		var num = $("#areaMenu a").index(this);
		$("#areaMap .map").hide();
		$("#areaMap .map").eq(num).show();
	});

	var ua = navigator.userAgent;

	var areaMenuLink = $('#areaMenu a').attr('xlink:href');
	var mobileUrl = areaMenuLink.replace('http://tohokukanko.tabi-kura.com/dom/s/','http://tohokukanko.tabi-kura.com/dom/ss/');
	if (ua.indexOf('iPhone') > 0 || ua.indexOf('Android') > 0 && ua.indexOf('Mobile') > 0) {
		$('#areaMenu a').attr('xlink:href', mobileUrl);
	} else if (ua.indexOf('iPad') > 0 || ua.indexOf('Android') > 0) {
		$('#areaMenu a').attr('xlink:href', mobileUrl);
	}
	var now = new Date();
	$("#calBtn").datetimepicker({
				lang:'ja',
				format:'Y/m/d',
				timepicker:false,
				closeOnDateSelect:true,
				scrollInput:false,
				yearStart:now.getFullYear(),
				yearEnd:now.getFullYear()+1,
				startDate:now, 
				minDate:'-1970/01/01', 
				maxDate:'+1970/06/01',
				onSelectDate:function(ct,$i){
					$('#sd').val(ct.dateFormat('Ymd'));
					$('#sd').trigger('change');
				}
	});
	
	var updateStayNights = function(){
		console.info($('#sd').val().length);
		if(0 < $('#sd').val().length) {
			$('#sn').prop('disabled','');
			$('#sn').attr('title','');
			$('#snNote').fadeOut();
		}
		else{
			$('#sn').prop('disabled','disabled');
			$('#sn').attr('title','泊数を指定するには、チェックイン日を指定します。');
			$('#snNote').show();
			$('#sn').val("1");
		}
	};
	//	泊数｢sn｣の指定には 、チェックイン日｢sd｣ の指定が必要
	$('#sd').on('change', function(){
		updateStayNights();
	});
	
	
	
	
	
	$("#clearArea").click(function(){
		$("input[name='ar']:checked").each(function() {
			$(this).removeAttr('checked');
		});
		return false;
	});
	
	$("#doSearch").on("click",function(){
		param = {};
		query = '';
		param["cp"] = $('#cp').val();
		param["rn"] = $('#rn').val();
		param["rt"] = $('#rt').val();
		param["pn"] = $('#pn').val();
		param["px"] = $('#px').val();
		param["mc"] = $('#mc').val();
		param["pf"] = $('#pf').val();
		param["kw"] = $('#kw').val();
		param["sn"] = $('#sn').val();
		if(!param["pf"]){
			$("#pf").addClass('flashWarning');
			setTimeout(function(){
				$("#pf").removeClass('flashWarning');
			},2000);
			return false;
		}
		if($('#sd').val().length > 0) {
			param["sd"] = $('#sd').val().replace(/\//g,'');
		}
		
		var checkKd = [];
		$( 'input[name="ha[]"]:checked' ).each( function() {
			checkKd.push('h_'+ $( this ).val() );
		});
		
		$( 'input[name="pa[]"]:checked' ).each( function() {
			checkKd.push('p_'+ $( this ).val() );
		});
		
		$.each(param,function(id,val){
			if(!(val==null)) {
				query += '&'+id+'='+encodeURIComponent(val);
			}
		});
		
		if(0<checkKd.length){
			query += ('&kd=' + checkKd.join(','));
		}

		//location.href = 'http://tohokukanko.tabi-kura.com/dom/s/?'+query;
		if (ua.indexOf('iPhone') > 0 || ua.indexOf('Android') > 0 && ua.indexOf('Mobile') > 0) {
			location.href = 'http://tohokukanko.tabi-kura.com/dom/ss/?'+query;
		} else if (ua.indexOf('iPad') > 0 || ua.indexOf('Android') > 0) {
			location.href = 'http://tohokukanko.tabi-kura.com/dom/ss/?'+query;
		} else {
			location.href = 'http://tohokukanko.tabi-kura.com/dom/s/?'+query;
		}
		return false;
	});
	
	nowDate = new Date();
	mm = nowDate.getMonth() + 1;
	dd = nowDate.getDate() + 1;
	$("#nowMonth").val(mm);
	$("#nowDay").val(dd);
	
	updateStayNights();
});
