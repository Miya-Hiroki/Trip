		} else {
			location.href = 'http://tohokukanko.tabi-kura.com/dom/s/?'+query;
		}
		return false;
	});
	
	nowDate = new Date();
	mm = nowDate.getMonth() + 1;
	dd = nowDate.getDate() + 1;
	$("#nowMonth").val(mm);
	$("#nowDay").val(dd);
	
	updateStayNights();
});
